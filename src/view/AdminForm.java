    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        if (txtKode.getText().isEmpty() || txtNama.getText().isEmpty() || "".equals(path) || Integer.parseInt(txtHarga.getText()) == 0 || Integer.parseInt(txtStok.getText()) == 0) {
            try {
                throw new Exception();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"Kolom kode, nama dan gambar dan lainnya tidak boleh kosong");
            }
        } else  {
            Hoodie p = new Hoodie();
            p.addProduct(txtKode.getText(), Integer.parseInt(idAdmin), txtNama.getText(), cmbTipe.getSelectedItem().toString()
                    , cmbUkuran.getSelectedItem().toString(),Integer.parseInt(txtHarga.getText()), Integer.parseInt(txtStok.getText()),
                    path);
            txtKode.setText("");
            txtNama.setText("");
            txtHarga.setText("");
            txtStok.setText("");
            dispose();
            this.setVisible(true);            
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        JFileChooser file = new JFileChooser();
        file.setMultiSelectionEnabled(true);
        file.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
        file.setFileHidingEnabled(false);
        if (file.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            java.io.File f = file.getSelectedFile();
            path = f.getPath().toString();
            txtPath.setText(path);
//            System.err.println(f.getPath().toString());
        }
    }//GEN-LAST:event_btnUploadActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
    // TODO add your handling code here:
        databaru = false; // menampilkan data ke textboxt
        try {
            int row =jTable1.getSelectedRow();
            String tabel_klik=(jTable1.getModel().getValueAt(row, 0).toString());
            java.sql.Connection conn =(java.sql.Connection)controller.koneksi.koneksiDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from karyawan where id_karyawan='"+tabel_klik+"'");
            if(sql.next()){
                String id = sql.getString("kodehoodie");
                txtKode.setText(id);
                String nama = sql.getString("nama");
                txtNama.setText(nama);
                String nik = sql.getString("tipe");
                cmbTipe.setSelectedItem(nik);
                String jabatan = sql.getString("ukuran");
                cmbUkuran.setSelectedItem(jabatan);
                String no_telp = sql.getString("harga");
                txtHarga.setText(no_telp);
                String alamat = sql.getString("stock");
                txtStok.setText(alamat);
                String path = sql.getString("gambar");
                txtPath.setText(path);
            }
        } catch (Exception e) {}
    }//GEN-LAST:event_jTable1MouseClicked

