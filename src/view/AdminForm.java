    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        Hoodie p = new Hoodie();
        if(!p.kodeCheck(txtKode1.getText())){
            JOptionPane.showMessageDialog(null, "Data Produk Tidak Ditemukan, Gagal Mengahpus Data");
        }else{
           p.deleteProduct(txtKode1.getText());
           txtKode1.setText("");
           GetData(); 
        }
        
    }//GEN-LAST:event_btnHapusActionPerformed

